// write function to see if character is a vowel //
// if char === a,e,i,o,u,A,E,I,O,U return true //
// it not, return false //

function isVowel(char) {
  if (char === 'a' || char === 'A' || char === 'e' || char === 'E' || char === 'i' || char === 'I' || char === 'o' || char === 'O' || char === 'u' || char === 'U') {
    return true;
  } else {
    return false;
  }
}
